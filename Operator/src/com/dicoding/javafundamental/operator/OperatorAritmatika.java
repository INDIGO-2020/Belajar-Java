package com.dicoding.javafundamental.operator;

/**
 *
 * @author AnggaAF
 */
public class OperatorAritmatika {
    public static void main(String[]args){
        System.out.println("Operasi Penjumlahan");
        int plus = 5 + 5;
        System.out.print("Hasil dari 5 + 5 = " + plus);
        System.out.println();
        
        System.out.println("Operasi Pengurangan");
        int min = 5 - 5;
        System.out.print("Hasil dari 5 - 5 = " + min);
        System.out.println();
        
        System.out.println("Operasi Perkalian");
        int times = 5 * 5;
        System.out.print("Hasil dari 5 * 5 = " + times);
        System.out.println();
        
        System.out.println("Operasi Pembagian");
        int selash = 5 / 5;
        System.out.print("Hasil dari 5 / 5 = " + selash);
        System.out.println();
        
        System.out.println("Operasi Mod");
        int modd = 13 % 2;
        System.out.print("Hasil dari 13 % 2 = " + modd);
        System.out.println();
        
    }
}
